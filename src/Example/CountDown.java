package Example;

public class CountDown {

    public static void main(String[] args) {

        int limit = 6;
        for (int i = 1; i < limit; ++i) {
            System.out.println(i + "");
        }
        System.out.println("__________");
        for (int i = 5; i > 0; --i) {
            System.out.println(i + "");


        }
    }
}
